/* SCP_Charge
 *
 * Controls the charging of the SuperCap.
/******************************************************************************/
inline void SCP_Charge(unsigned char action)
{
    if (action)
    {
        LATD |= ChargeCap;
    }
    else
    {
        LATD &= ~ChargeCap;
    }
}
